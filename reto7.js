/** @param {string} packages with parentheses
 *  @returns {string} Fixed and sorted packages
 */
function fixPackages(packages) {
    //para ir almacenando las partes
    let pila = []
    let parteActual = ''

    for(let i = 0; i<packages.length; i++){
    
        let caracter = packages[i]
        switch(caracter){

            case '(':
                //se aÃ±ade a la pila la parte actual y esta se reinicia
                pila.push(parteActual)
                parteActual = ''
                break
            case ')':
                //se aÃ±ade lo que estaba en la pila, y la parte actual se le da la vuelta
                parteActual = pila.pop() + parteActual.split('').reverse().join('')
                break
            default:
                //se aÃ±ade el caracter a la parte actual
                parteActual += caracter
        }
    }

    return parteActual
}

